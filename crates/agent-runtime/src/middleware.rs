/// Trait for middleware that preprocesses messages
#[async_trait]
pub trait PreProcessMiddleware: Send + Sync {
    /// Process messages before sending to LLM
    async fn process(
        &self,
        messages: Vec<ChatMessage>,
        ctx: &MiddlewareContext,
    ) -> MiddlewareResult<Vec<ChatMessage>>;
}

/// Trait for middleware that handles events
pub trait EventMiddleware: Send + Sync {
    /// Handle an event during execution
    fn handle_event(&self, event: &MiddlewareEvent);
}

// ============================================================================
// TYPES
// ============================================================================

/// Result from middleware processing
pub struct MiddlewareResult<T> {
    /// The processed data
    pub data: T,

    /// Events to emit
    pub events: Vec<MiddlewareEvent>,
}

/// Context for middleware execution
#[derive(Clone, Default)]
pub struct MiddlewareContext {
    /// Agent ID
    pub agent_id: Option<String>,

    /// Conversation ID
    pub conversation_id: Option<String>,

    /// Maximum tokens allowed
    pub max_tokens: Option<usize>,

    /// Current token count
    pub token_count: usize,
}

/// Events generated by middleware
#[derive(Debug, Clone)]
pub enum MiddlewareEvent {
    /// Summarization event
    Summarization {
        original_count: usize,
        summarized_count: usize,
    },

    /// Context editing event
    ContextEdited {
        cleared_count: usize,
    },
}

// ============================================================================
// MIDDLEWARE PIPELINE
// ============================================================================

/// Pipeline that orchestrates middleware execution
pub struct MiddlewarePipeline {
    preprocessors: Vec<Box<dyn PreProcessMiddleware>>,
    event_handlers: Vec<Box<dyn EventMiddleware>>,
}

impl MiddlewarePipeline {
    /// Create a new middleware pipeline
    #[must_use]
    pub fn new() -> Self {
        Self {
            preprocessors: Vec::new(),
            event_handlers: Vec::new(),
        }
    }

    /// Add a preprocessor
    pub fn add_preprocessor(&mut self, processor: Box<dyn PreProcessMiddleware>) {
        self.preprocessors.push(processor);
    }

    /// Add an event handler
    pub fn add_event_handler(&mut self, handler: Box<dyn EventMiddleware>) {
        self.event_handlers.push(handler);
    }

    /// Process messages through all preprocessors
    pub async fn process_messages(
        &self,
        messages: Vec<ChatMessage>,
        ctx: &MiddlewareContext,
    ) -> Result<Vec<ChatMessage>, MiddlewareError> {
        let mut current_messages = messages;
        let mut all_events = Vec::new();

        for processor in &self.preprocessors {
            let result = processor.process(current_messages, ctx).await;
            current_messages = result.data;
            all_events.extend(result.events);
        }

        Ok(current_messages)
    }

    /// Handle an event through all event handlers
    pub fn handle_event(&self, event: &MiddlewareEvent) {
        for handler in &self.event_handlers {
            handler.handle_event(event);
        }
    }
}

impl Default for MiddlewarePipeline {
    fn default() -> Self {
        Self::new()
    }
}

/// Middleware errors
#[derive(Debug, thiserror::Error)]
pub enum MiddlewareError {
    #[error("Middleware processing failed: {0}")]
    ProcessingFailed(String),
}

